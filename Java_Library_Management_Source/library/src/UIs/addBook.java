    public void getDataFromUser() {
        try {
            int sabt = Integer.parseInt(jFsabt.getText());
            String saleNashr = jFsalenashr.getText().replace('/', '-');
            String onvan = jTonvan.getText();
            String nevisande = jTnevisande.getText();
            String nasher = jTnasher.getText();
            String mahaleNashr = jTmahalnashr.getText();
            String zaban = jTzaban.getText();
            long arzesh = Long.parseLong(jTarzesh.getText());
            String shabak = jTshabak.getText();
            String kholose = jEkholase.getText();
            String mozo = jTmozo.getText();
            String jeld = jTjeld.getText();
            int tedad = (int) jStedad.getValue();

            Book book = new Book(sabt, saleNashr, onvan, nevisande, nasher,
                    mahaleNashr, zaban, arzesh, shabak, kholose, mozo,
                    jeld, tedad);
            if (isAdd) {
                book.insertBook();
            } else {
                book.updateBook();
            }
        } catch (Exception e) {
            System.err.println(e);
        }
    }

