    private void loadFrom() {
        jTMid.setText("" + rezerv.getMid());
        jTBsabt.setText("" + rezerv.getBsabt());
        jTDate.setText("" + rezerv.getDate());
        jTRetDate.setText(getRetDate());
    }

    private void getDataFromUser() {
        rezerv.setMid(Integer.parseInt(jTMid.getText()));
        rezerv.setRetdate(jTRetDate.getText());
    }

    private String getRetDate() {
        MyDate today = new MyDate();
        for (int i = 0; i < 5; i++) {
            today.nextDay();
        }
        return today.getIranianDate().replace('/', '-');
    }

