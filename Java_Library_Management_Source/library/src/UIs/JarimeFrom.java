    private void loadFrom() {
        DataAcces da = new DataAcces("select * from ROOT.NJARIME");
        this.data = da.getData();
        String[] njarime = new String[data.length];
        for (int i = 0; i < data.length; i++) {
            njarime[i] = data[i][1];
        }
        jTdate.setText(new MyDate().getIranianDate().replace('/', '-'));
        jCnkod.setModel(new DefaultComboBoxModel(njarime));
    }

    private void getDataFromUser() {
        jarime = new Jarime(
                Integer.parseInt(jTmid.getText()),
                jTdate.getText(),
                Integer.parseInt(data[indexNkod][0]), // 0: nkod
                Integer.parseInt(jTukod.getText()));
    }
