    private void loadForm() {
        if (amanat != null) {
            jTukod.setText("" + amanat.getUkod());
            jTMid.setText("" + amanat.getMid());
            jTBSabt.setText("" + amanat.getBsabt());
            jTDate.setText(amanat.getDate());
            jTRetDate.setText(getRetDate());
        } else {
            jTDate.setText(new MyDate().getIranianDate().replace('/', '-'));
        }
    }

    private void getDataFromUser() {
        if (amanat != null) {
            amanat.setMid(Integer.parseInt(jTMid.getText()));
            amanat.setRetdate(jTRetDate.getText());
        } else {
            amanat = new Amanat(Integer.parseInt(jTukod.getText()),
                    Integer.parseInt(jTMid.getText()),
                    Integer.parseInt(jTBSabt.getText()),
                    jTDate.getText(),
                    jTRetDate.getText());

        }
    }

    private String getRetDate() {
        MyDate today = new MyDate();
        for (int i = 0; i < 15; i++) {
            today.nextDay();
        }
        return today.getIranianDate().replace('/', '-');
    }
