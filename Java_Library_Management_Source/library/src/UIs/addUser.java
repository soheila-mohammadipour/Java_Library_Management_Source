    public void getDataFromUser() {
        try {
            int kod = Integer.parseInt(jFkod.getText());
            String nam = jTnam.getText();
            String famil = jTfamil.getText();
            String semat = (String) jCsemat.getSelectedItem();
            int dastresi = jCdastresi.getSelectedIndex() + 1;
            String userName = jTuserName.getText();
            String pass = jPpass1.getText();

            User user = new User(kod, nam, famil, semat, dastresi, userName, pass);

            if (isAdd) {
                user.insertUser();
            } else {
                user.updateUser();
            }

        } catch (Exception e) {
            System.err.println(e);
        }
    }

    public void clearForm() {
        jFkod.setText("");
        jTnam.setText("");
        jTfamil.setText("");
        jCsemat.setSelectedIndex(-1);
        jCdastresi.setSelectedIndex(-1);
        jTuserName.setText("");
        jPpass1.setText("");
        jPpass2.setText("");
    }

    public void loadForm() {
        jFkod.setText((String) Handle.getItem(User.UserIndex.KOD));
        jTnam.setText((String) Handle.getItem(User.UserIndex.NAM));
        jTfamil.setText((String) Handle.getItem(User.UserIndex.FAMIL));
        jTuserName.setText((String) Handle.getItem(User.UserIndex.USERNAME));
    }
