    private void loadForm() {
        jTnkod.setText((String) Handle.getItem(NJarime.NJarimeIndex.NKOD));
        jTnjarime.setText(
                (String) Handle.getItem(NJarime.NJarimeIndex.NJARIME));
        jTnmizan.setText(
                (String) Handle.getItem(NJarime.NJarimeIndex.NMIZAN));
    }

    private void getDataFromUser() {
        njarime = new NJarime(
                Integer.parseInt(jTnkod.getText()),
                jTnjarime.getText(),
                Integer.parseInt(jTnmizan.getText()));
    }

    private void clearFrom() {
        jTnkod.setText("");
        jTnjarime.setText("");
        jTnmizan.setText("");
    }
