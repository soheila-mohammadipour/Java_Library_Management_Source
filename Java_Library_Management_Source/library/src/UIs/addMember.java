    public void getDataFromUser() {
        try {
            int id = Integer.parseInt(jFid.getText());
            String nam = jTname.getText();
            String famil = jTfamil.getText();
            long kod = Long.parseLong(jFkod.getText());
            String namPedar = jTnamPedar.getText();
            String tavalod = jFtavalod.getText().replace('/', '-');
            long shomare = Long.parseLong(jFshomare.getText());
            String jensiat;
            if (jRmale.isSelected()) {
                jensiat = "مرد";
            } else {
                jensiat = "زن";
            }
            String adres = jEadress.getText();
            String reshte = jTreshte.getText();
            String aks = file.getAbsolutePath();
            String email = jTemail.getText();
            String tarikhEn = jFtarikhEn.getText().replace('/', '-');

            Member member = new Member(id, nam, famil, kod, namPedar, tavalod,
                    shomare, jensiat, adres, reshte, aks, email, tarikhEn);

            if (isAdd) {
                member.insertMember();
            } else {
                member.updateMember();
            }
        } catch (Exception ex) {
            System.err.println(ex);
        }
    }

